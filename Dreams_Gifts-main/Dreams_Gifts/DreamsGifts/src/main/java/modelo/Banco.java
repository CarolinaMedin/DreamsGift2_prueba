package modelo;

/**
 *
 * @author Usuario
 */
public class Banco {
    public int id;   
    private String descripcion;
    private boolean estado;

    public Banco() {
    }

    public Banco(int id, String descripcion, boolean estado) {
        this.id = id;
        this.descripcion = descripcion;
        this.estado = estado;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public boolean isEstado() {
        return estado;
    }

    public void setEstado(boolean estado) {
        this.estado = estado;
    }

    @Override
    public String toString() {
        return "Banco{" + "id=" + id + ", descripcion=" + descripcion + ", estado=" + estado + '}';
    }
    
}
