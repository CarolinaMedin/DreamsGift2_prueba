    //Metodo guardar
    protected void guardar(){   
        String mensajeError="";
        String query="";
        PreparedStatement preparar=null;
        if(txtUsuarioNombre.getText().isEmpty()==true){
            mensajeError=mensajeError+" Ingrese Nombre, ";
        }
        if(txtUsuarioUserName.getText().isEmpty()==true){
            mensajeError=mensajeError+" Ingrese Nombre de Usuario, ";            
        }
        if(txtUsuariosClave.getText().isEmpty()==true){
            mensajeError=mensajeError+" Ingrese Clave, ";
        }
        if(mensajeError.isEmpty()==true){
              query="insert into usuarios (USU_NOMBRE, USU_USERNAME, USU_CLAVE ) values ('"+txtUsuarioNombre.getText()+"','"+txtUsuarioUserName.getText()+"','"+txtUsuariosClave.getText()+"')"; 
            try{
                preparar=con.prepareStatement(query);
                preparar.executeUpdate();
                cargartabla();
            }catch (Exception e) {
                
            }
        }
        else{
            JOptionPane.showMessageDialog(null, mensajeError);
        }
    }//GEN-LAST:event_btnUsuariosGuardarActionPerformed

