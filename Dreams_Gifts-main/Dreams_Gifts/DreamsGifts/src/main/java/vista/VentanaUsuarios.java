    protected void cargartabla(){

        modelo.setRowCount(0);
        String datos[]=new String[6];
        String query="select * from usuarios";
        ResultSet rs;
        try {
            ejecutar=con.createStatement();            
            rs=ejecutar.executeQuery(query);
            while(rs.next()==true){
                datos[0]=rs.getString("USU_ID_USUARIO");
                datos[1]=rs.getString("USU_NOMBRE");
                datos[2]=rs.getString("USU_APELLIDO");
                datos[3]=rs.getString("USU_USERNAME"); 
                datos[4]=rs.getString("USU_CLAVE");
                datos[5]=rs.getString("USU_ESTADO"); 
                modelo.addRow(datos);
            }
            tablaUsuarios.setModel(modelo);
        } catch (Exception e){            
        }        
    }
    
    
    //Mostrar si hay conexion con metodo conectar
    public void conectar(){
        con=null;
        try {
            Class.forName(driver);
            con=(Connection)DriverManager.getConnection(url, user, pass);
            if(con!=null){
               txtInformacion.setText("Conexion exitosa"); 
            }            
        }catch (Exception e){ 
               txtInformacion.setText("Sin conexion!!! "+e);
        }
    }

